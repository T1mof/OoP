package com.example.apipoller.model;

import java.util.Map;

/**
 * Интерфейс для записей данных, получаемых от API.
 * Все конкретные реализации должны предоставить уникальный идентификатор
 * и способ сериализации в Map для последующей записи в файл.
 */
public interface ApiRecord {
    /**
     * Возвращает уникальный идентификатор записи
     * @return строковый идентификатор
     */
    String getId();
    
    /**
     * Преобразует запись в карту для сериализации
     * @return карта с полями записи
     */
    Map<String, Object> toMap();
}
